import math

# masukkan angka bulat
angka_bulat = int(input("Masukkan angka bulat: "))

# membagikan angka bulat dengan 3
hasil_bagi = angka_bulat / 3

# membulatkan hasil bagi ke atas dengan tiga tempat desimal
hasil_bulat = round(hasil_bagi, 3)

# tampilan hasil
print(f"Hasil bagi adalah: {hasil_bulat}")